+ (UIImage*)scaleImage:(UIImage*)aImage ToSize:(CGSize)aSize
{
    float iImageWidth=aSize.width;
    float iImageHeight=aSize.height;
    // 创建一个bitmap的context
    // 并把它设置成为当前正在使用的context
    CGSize size = CGSizeMake(iImageWidth, iImageHeight);
    if([[UIScreen mainScreen] scale] == 2.0){                UIGraphicsBeginImageContextWithOptions(size, NO, 2.0);
    }else{
        UIGraphicsBeginImageContext(size);
    }
    // 绘制改变大小的图片
    [aImage drawInRect:CGRectMake(0, 0, iImageWidth, iImageHeight)];
    // 从当前context中创建一个改变大小后的图片
    UIImage* scaledImage = UIGraphicsGetImageFromCurrentImageContext();
    // 使当前的context出堆栈
    UIGraphicsEndImageContext();
    // 返回新的改变大小后的图片
    return scaledImage;
}


